


• переменные типа bool устанавливаются в false ;
• переменные числовых типов устанавливаются в О (или в О.О для типов с плавающей точкой);
• переменные типа char устанавливаются в одиночный пустой символ;
• переменные типа Biginteger устанавливаются в О ;
• переменные типа DateTime устанавливаются в 1/1/0001 12 : 00 : 00 АМ;
• объектные ссылки (включая переменные типа string) устанавливаются в null .