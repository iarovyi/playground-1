Спецификация CLS - это набор правил, подробно описывающих минимальное и полное множество характеристик, которые отдельный компилятор .Net должен поддерживать, чтобы генерировать код, обслуживаемый средой CLR и в то же время доступный в унифицированной манере всем языкам, которые ориентированы на платформу .Net

Правила CLS
Правила CLS применяются только к тем частям типа, которые видны извне определяющей сборки
// открытые для доступа данные без знака не совместимы с CLS
