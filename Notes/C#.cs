Visual Studio - программное обеспечение, разработанное компанией Microsoft, чтобы писать программы на c# и других языках.

ООП - парадигма программ, в которой основными концепциями являются понятия классов и объектов.

Класс - это конструкция языка, состоящая из ключевого слова class, идектификатора (имени) и тела.
Класс может содерджать в своем теле: поля, методы, свойства и события.
Поля определяют состояние, а методы поведение будущего объекта. (instances - экземпляры)
Объекты содержат в себе статистическиепоял и все методы.
Экземпляры содержат нестатистические поля. 
MyClass instance1 = new MyClass ()
Ключевое слова new говорит, что мы хотим выделить область памяти на куче (Heap)
ссылка - первый байт в памяти

 Объект - область памяти, которая хранит в себе методы и статические поля.
 Экземпляр - область памяти, которая хранит в себе нестатические поля.
