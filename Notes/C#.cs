Исполняющая среда (CLR) - коллекция служб, которые требуются для выполнения сомпилированной единицы кода
- занимается определением месторасположения сборки
- нахождение запрошенного типа
- размещает тип в памяти, компилирует код в специф.инструкции
- проверка безопасности
-выполняет код

