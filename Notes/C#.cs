Console - это обьект, к которому обращаемся. Это клавиатура и экран.
Все обьекты имеют состояние (внешний вид) и поведение.

Переменная - логическая ячейка, хранит инфо, имеет определенный тип.

Операции над переменными:
+ присваивание (=)
+ арифметические операции (+,-,*,/)
+ комбинированные (+=, -=, *=, /=)
+ логические операции

